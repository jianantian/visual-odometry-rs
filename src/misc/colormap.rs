//! Colormap helpers for visualizations.

/// Viridis colormap in an RGB u8 vector.
pub fn viridis_u8() -> Vec<(u8, u8, u8)> {
    viridis()
        .into_iter()
        .map(|(r, g, b)| (to_u8(r), to_u8(g), to_u8(b)))
        .collect()
}

fn to_u8(x: f32) -> u8 {
    (255.0 * x).round() as u8
}

